        <h1 class="text-center">Melahirkan Lebih Lancar, Ini Berbagai Teknik Persalinan yang Meringankan Rasa Sakit</h1>
        <blockquote class="text-justify">
            <p>Perempuan yang segera menjadi ibu biasanya dirundung kekhawatiran akan rasa sakit
                saat melahirkan. Rasa nyeri yang tak tertahankan biasanya muncul ketika terjadi
                kontraksi sampai saat proses melahirkan berlangsung. Oleh sebab itu, berbagai
                metode biasanya diterapkan sebelumnya agar melahirkan berjalan lebih lancar.
                Mulai dari senam, jalan kaki sampai penggunaan obat bius yang meredakan rasa sakit.
                Namun, sekarang ini terdapat juga pilihan teknik persalinan yang bisa dilakukan untuk
                mengurangi rasa sakit.</p>

            <h4>1. Teknik persalinan dengan anestesi</h4>
            <p>Pemberian anestesi atau obat bius menjadi metode yang paling umum dilakukan untuk mengurangi
                rasa sakit saat melahirkan. Terlebih pada ibu yang kondisi kesehatannya lemah sehingga tidak
                memiliki tingkat toleransi yang tinggi terhadap rasa sakit. Terdapat beberapa jenis anestesi
                yang diperbolehkan untuk proses melahirkan. Namun anestesi lokal yang hanya membius bagian
                sekitar vagina kurang efektif dalam mengatasi rasa sakit.</p>
            <h5>Anestesi epidural</h5>
            <p>Metode anestesi ini paling sering dilakukan ketika proses melahirkan. Obat bius disuntikkan ke
                dalam tubuh pada bagian sumsum tulang belakang dan menyebabkan pinggul ke bawah terasa kebas efek
                dari anestesi. Umumnya, obat ini diberikan ketika pembukaan ke-4 atau 5 terjadi. Anestesi epidural
                juga dilakukan ketika ibu melahirkan dengan operasi caesar. Meski mengurangi rasa sakit, biasanya
                cara ini tidak akan dilakukan jika ibu mengalami pendarahan atau penurunan tekanan darah.</p>
            <h5>Anestesi intratekal</h5>
            <p>Terdapat jenis anestesi lain yang bisa dijadikan alternatif, yaitu anestesi intratekal yang
                memiliki dosis lebih rendah, tapi tidak kalah ampuh. Dosis yang tidak terlalu besar ini dapat
                mengurangi efek samping dari anestesi yang dilakukan. Akan tetapi anestesi hanya bisa bertahan
                sekitar 4 jam sehingga sebaiknya tidak dipakai dalam persalinan yang membutuhkan waktu lama.
                Menurut ulasan NCBI, anestesi intratekal terdiri atas:</p>
            <ul>
                <li>Fentanyl sebanyak 0,025 mg yang mampu mempercepat proses pembukaan dan mempersingkat
                    tahap persalinan pertama</li>
                <li>Bupivacaine sejumlah 2,5 mg yang membantu mengatasi rasa nyeri di bagian perut di
                    persalinan tahap kedua</li>
                <li>Morfin sebesar 0,25 mg yang berfungsi memperpanjang efek bius </li>
            </ul>
            <h5>Efek samping anestesi</h5>
            <p>Meskipun jarang terjadi, efek samping dari anestesi tetap perlu diwaspadai sedari awal.
                Beberapa gangguan kesehatan ringan yang mungkin ditimbulkan teknik persalinan menggunakan
                anestesi adalah rasa mual, gatal-gatal, dan sakit kepala. Efek samping ini bisa diatasi
                dengan memberikan obat intravena (disuntikan melalui pembuluh vena) dan obat naltrexone. </p>

            <h4>2. Gas Nitrogen Oksida (N2O) </h4>
            <p>Dikenal dengan nama gas tertawa, nitrogen oksida memang merupakan gas yang bersifat analgesik
                yang secara efektif mengurangi rasa nyeri. Metode melahirkan dengan gas nitro oksida juga
                dikenal dengan anestesi entonoks. Pada entonoks terdapat juga campuran oksigen. Entonoks
                dapat dihirup melalui masker. Biasanya dokter akan meminta Anda untuk mengambil napas
                dalam-dalam secara perlahan agar gas ini dapat memenuhi paru-paru. </p>
            <p>Setelah itu gas akan diteruskan ke otak untuk memengaruhi saraf nyeri sehingga rasa sakit
                bisa seketika mereda. Sebagian besar perempuan yang menjalani teknik persalinan ini sangat
                puas terhadap efek penghilang nyeri yang muncul ketika terjadi kontraksi.</p>

            <h4>3. Hypnobirthing</h4>
            <p>Metode melahirkan yang mengandalkan hipnosis dan relaksasi saat ini juga mulai marak dilakukan.
                Pasalnya, teknik tersebut memang membantu para perempuan yang hendak melahirkan mempersiapkan diri
                secara mental dan fisik. Hipnosis dilakukan untuk mengurangi kesadaran seseorang akan ketakutan,
                kegelisahan, dan kecemasan akan rasa sakit saat proses melahirkan. Teknik persalinan seperti ini
                sebenarnya telah dilakukan selama berabad-abad demi kelancaran proses melahirkan. Namun memang
                mustahil bagi tubuh untuk merasa rileks ketika kontraksi atau proses melahirkan berlangsung. </p>
            <p>Oleh karena itu, metode persalinan ini membutuhkan latihan agar tubuh Anda terbiasa untuk rileks.
                Tujuannya adalah melancarkan pelepasan oksitosis, hormon prostaglandin, dan endorfin di dalam otak
                sehingga otot-otot mengendur dan tubuh terasa nyaman. Dengan begitu, Anda juga bisa lebih mengendalikan
                sensasi nyeri yang muncul saat melahirkan.</p>
        </blockquote>
