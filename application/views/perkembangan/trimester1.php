            <h4>Perkembangan Kehamilan di Minggu ke-6</h4>
            <p>Pada usia 6 minggu kehamilan ini, Anda akan merasa lebih mudah lelah, seperti dilansir Kids Health. Anda mungkin akan merasakan lebih banyak
                tanda-tanda kehamilan daripada minggu lalu. Berikut beberapa tips untuk membantu Anda melewati periode ini.</p>
            <h5>Perhatikan tubuh Anda</h5>
            <p>Seperti yang disebutkan sebelumnya, Anda akan lebih cepat lelah dalam fase perkembangan janin kehamilan 6 minggu. Jadi, kalau Anda merasa
                lelah, sebaiknya segera beristirahat. Jangan memaksakan diri untuk bekerja atau melakukan aktivitas fisik lainnya.</p>
            <h5>Minta bantuan</h5>
            <p>Jangan sungkan untuk meminta bantuan pada suami Anda. Bila perlu, sampaikan apa yang sedang Anda rasakan kepada pasangan Anda. Jika teman atau
                keluarga menawarkan bantuan jangan menolaknya agar kondisi Anda dan bayi tetap terjaga.</p>
            <h5>Tidur yang cukup</h5>
            <p>Ibu hamil sering merasa lebih mudah mengantuk. Untuk mengatasinya, luangkanlah lebih banyak waktu untuk tidur. Jika memungkinkan, tidurlah
                lebih awal agar waktu tidur Anda bisa lebih lama. Selain tidak mengganggu aktivitas sehari-hari, tidur yang cukup juga membantu
                mengoptimalkan perkembangan janin.</p>
            <h5>Makan teratur</h5>
            <p>Untuk menjaga energi, Anda memerlukan pasokan bahan bakar dari makanan. Pastikan Anda memenuhi asupan protein dan karbohidrat selama kehamilan.
                Selain itu, pastikan juga Anda mendapatkan cukup kalori agar perkembangan janin 6 minggu kehamilan tetap terjaga dengan baik.</p>
            <h5>Pilih makanan untuk kurangi rasa mual</h5>
            <p>Rasa mual dan ingin muntah sangat wajar terjadi di trimester pertama, termasuk di usia 6 minggu kehamilan. Rasa mual dapat membuat Anda
                tidak nafsu makan. Pilih makanan yang mudah Anda makan seperti bubur dan sup. Anda bisa tambahkan sedikit jahe untuk membantu mengurangi
                rasa mual. Bisa juga dengan mengonsumsi makanan porsi sedikit tapi sering. Terkadang makan dengan porsi banyak membuat rasa mual justru muncul.</p>
            <h5>Terus bergerak</h5>
            <p>Kehamilan bukan alasan untuk tidak melakukan aktivitas sama sekali. Berusahalah untuk tetap bergerak dengan melakukan berbagai aktivitas fisik
